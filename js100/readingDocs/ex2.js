// Find out whether JavaScript has a method to lowercase a string, for example converting 'Aloha, World!' into 'aloha, world!.

let str = 'Aloha, World!';
let low = str.toLowerCase();
console.log(str); // 'Aloha, World!'
console.log(low); // 'aloha, world!'

// Yes there is and it is immutable. The original string stays the same! 